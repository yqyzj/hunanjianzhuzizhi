# 生成FAQ页面
def generate_faq_pages():
    # 按ID排序，确保页码连续
    sorted_faqs = sorted(faq_data, key=lambda x: x["id"])
    
    for i, faq in enumerate(sorted_faqs):
        # 生成上一篇/下一篇链接
        prev_link = ''
        next_link = ''
        
        if i > 0:
            prev_faq = sorted_faqs[i-1]
            prev_link = f'''<a href="{prev_faq['id']}.html" class="nav-prev">
                <div class="nav-label">上一篇</div>
                <div class="nav-title">{prev_faq['title']}</div>
            </a>'''
        else:
            prev_link = f'''<div class="nav-prev nav-disabled">
                <div class="nav-label">上一篇</div>
                <div class="nav-title">没有了</div>
            </div>'''
            
        if i < len(sorted_faqs) - 1:
            next_faq = sorted_faqs[i+1]
            next_link = f'''<a href="{next_faq['id']}.html" class="nav-next">
                <div class="nav-label">下一篇</div>
                <div class="nav-title">{next_faq['title']}</div>
            </a>'''
        else:
            next_link = f'''<div class="nav-next nav-disabled">
                <div class="nav-label">下一篇</div>
                <div class="nav-title">没有了</div>
            </div>'''
        
        # 渲染页面内容
        content = FAQ_TEMPLATE.format(
            id=faq["id"],
            title=faq["title"],
            conclusion=faq["conclusion"],
            content=faq["content"],
            prev_link=prev_link,
            next_link=next_link
        )
        
        # 写入文件
        output_path = os.path.join(FAQ_DIR, f"{faq['id']}.html")
        with open(output_path, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"已生成 FAQ 页面: {faq['id']}.html")

if __name__ == "__main__":
    generate_faq_pages()
    print("FAQ 页面生成完成!")